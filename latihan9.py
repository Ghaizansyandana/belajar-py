class Manusia:
    def __init__(self, nama, umur):
        self.nama = nama
        self.umur = umur

    def perkenalan(self):
        print(f"Halo, nama saya {self.nama} dan umur saya {self.umur}") 

manusia1 = Manusia("gojin", 10) 
manusia2 = Manusia("anjing", 69)

manusia1.perkenalan()
manusia2.perkenalan()
