# if

umur = 10  

if umur >= 12:
    print ("sudah remaja")
else:
    print ("masih kecil")

# for

for i in range(5):
    print("hello", i)

# while

angka = 0
while angka < 5:
    print("angka sekarang", angka)
    angka += 1

# Penjelasan
# selama angka < 5, blok dialam while akan dijalankan
# kalau sudah angka == 5, perulangan berhenti